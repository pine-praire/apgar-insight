export function calculateProcrastinationResult(answers: ProcrastinationType[]): ProcrastinationType[] {
  const counts = answers.reduce(
    (acc, type) => {
      acc[type] = (acc[type] ?? 0) + 1
      return acc
    },
    {} as Partial<Record<ProcrastinationType, number>>,
  )
  const max = Math.max(...Object.values(counts) as number[])
  return (Object.keys(counts) as ProcrastinationType[]).filter((t) => counts[t] === max)
}
